package day04_concatination;

public class Concatenation_vs_Addition {

    public static void main(String[] args) {
        System.out.println("32" + 1); // result will be 321 <----- its concatenation
        //for Concatenation you must have String

        System.out.println("____________________________");

        //----Addition
        System.out.println(32 + 5); // result will be 37 <------its addition
        // for Addition you must have only numbers
    }

}
