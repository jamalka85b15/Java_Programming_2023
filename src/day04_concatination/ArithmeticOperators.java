package day04_concatination;

public class ArithmeticOperators {

    public static void main(String[] args) {
        System.out.println( 200 + 100); //  Addition
        System.out.println(500 - 200); //Subtraction
        System.out.println(800 / 2); //Division
        System.out.println(50 / 6); // int / int = int
        System.out.println(50 / 6.0); // int / double = double
        System.out.println(50d / 6); //double / int = double
        System.out.println(50.0 / 6.0); //double /double = double
        System.out.println(300 * 2); //Multiplication

        System.out.println(10 % 3); //Module is implicitly using numerator - (denominator * integer result)
        System.out.println(20 % 6);
        System.out.println(15 % 4);


    }

}
